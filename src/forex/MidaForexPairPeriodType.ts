// Represents the time interval (expressed in seconds) of a period.
export enum MidaForexPairPeriodType {
    // 1 minute.
    M1 = 60,

    // 5 minutes.
    M5 = 300,

    // 15 minutes.
    M15 = 900,

    // 30 minutes.
    M30 = 1800,

    // 1 hour.
    H1 = 3600,

    // 1 day.
    D1 = 86400,

    // 1 week.
    W1 = 604800,
}
